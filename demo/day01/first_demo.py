# 变量名是什么
# =赋值符
# type是方法名
# 需要一个变量作为参数
# type()方法的作用是查看变量类型
# 整数
a = 1
print(type(a))
# 小数
a = 1.1
print(type(a))
# 字符串
c = 'kkk'
print(type(c))
d = "sss"
print (type(d))
e = '''ooo'''
print (type(e))
f = """ppp"""
print(type(f))

# 列表list
s = [1,2.3]
print(1,2,3)
print(type(s))

# tuple 元组 和列表一致，单个时，用逗号结束
z = (2,)
print(z)
print(type(z))

# 字典 dict
kk = {"名字":"牛牛","组别":"牛组"}
print(type(kk))
print(kk)
